def banner(*, version: str | None = None) -> str:
    """Full high-detail splash for console boot / `banner` command."""
    ver = version if version is not None else __version__
    return (
        f"{_CASTLE}\n"
        f"\n"
        f"  C2 desk for Reach · channel · agents · plane · defend the keep\n"
        f"  Hogwarts v{ver}  ·  type help"
    )


def banner_short() -> str:
    """Compact lettermark after `clear`."""
    return (
        f"{_LETTERMARK}\n"
        f"  Hogwarts · C2 keep · type help"
    )
